package u02_UsingObjects;

public class s13_WritingMethodsThatReturn {

    public static void main(String[] args){
        int i = 0;
        while(i<10){
            i++; //i = i + 1
            System.out.println(i);
        }

        int j = 0;
        while(j<10){
            j++; //i = i + 1
            System.out.println(j);
            if(j==5){
                break;
            }
        }




    }


}
